<template>
  <section class="main-content">
    <div class="container-fluid">
      <div class="row justify-content-center">
        <div class="col-md-5">
          <b-button
            variant="grey"
            size="sm"
            :to="{ name: 'account' }"
            class="mb-3"
          >
            <i class="fa fa-angle-left fa-fw"></i>
            Back
          </b-button>

          <h1>Create Note</h1>

          <div class="form-group">
            <label>Title</label>
            <input type="text" class="form-control" v-model="note.title" />
          </div>

          <div class="form-group">
            <label>Content</label>
            <textarea class="form-control" v-model="note.content"></textarea>
          </div>
          <button
            id="create-note-button"
            @click="createNote"
            class="btn btn-primary btn-block"
          >
            <i class="fa fa-save fa-fw"></i> Save
          </button>
        </div>
      </div>
    </div>
  </section>
</template>

<script>
import { mapGetters } from "vuex";

export default {
  name: "editNote",
  data() {
    return {
      note: {
        title: "",
        content: ""
      }
    };
  },
  methods: {
    async createNote() {
      if (!this.note.title || !this.note.content) {
        this.$toasted.error("Title and Content need to be filled out.");
        return;
      }

      try {
        const response = await this.$store.dispatch(
          "note/createNote",
          this.note
        );
        await this.$store.dispatch(
          "note/addNoteToStack",
          response.data.data.note
        );
        this.$router.push({ name: "account" });
        this.$toasted.success("Note created.");
      } catch (error) {
        this.$toasted.error("There was an error connecting to the server.");
      }
    }
  },
  computed: {
    ...mapGetters({
      user: "user/user",
      notes: "note/notes"
    })
  },
  created() {}
};
</script>

